/* Lightbox Close Button */

function closeLightbox(){
	$('#SBA_expand_lightbox').removeClass('SBA_expand_show');
	$('#SBA_expand_lightbox_left').empty();
	$('#SBA_expand_lightbox_right_content').empty();
}

$('#SBA_expand_lightbox_close').click(function(){
   closeLightbox();
});


