function lightbox(title) {

  $.each(product.Item, function(index){
	if(this.Title == title) {
	  $('#SBA_expand_lightbox_right_content').append('<h2 class="SBA_expand_product_price">' + this.Price + '<h2/>');
	  $('#SBA_expand_lightbox_right_content').append('<h3 class="SBA_expand_product_Subhead">' + this.Subhead + '<h3/>');
	  $('#SBA_expand_lightbox_right_content').append('<h1 class="SBA_expand_product_title">' + this.Title + '<h1/>');
	  $('#SBA_expand_lightbox_right_content').append('<p class="SBA_expand_product_Desc">' + this.Desc + '<p/>');
	  $('#SBA_expand_lightbox_right_content').append('<a href="' + this.Link_url + '"class="SBA_expand_product_cta">' + this.Link_text + '<a/>');
	  $('#SBA_expand_lightbox_left').append('<img data-title="'+ this.Title +'" src="' + this.Image_url + '" style="width:' + this.Lightbox_Image_Width + '; top:' + this.ProductTop + '; left:' + this.ProductLeft + '; position:relative;" / ><br/>');
	  if(this.LogoImage_Url != null){
		$('#SBA_expand_lightbox_left').append('<img data-title="'+ this.Title +'" src="' + this.LogoImage_Url + '" style="top:' + this.LogoTop + '; left:' + this.LogoLeft + '; position:relative;" / ><br/>');
	  }
	  $('#SBA_expand_lightbox').addClass('SBA_expand_show');
	}
  });
}


